#include "ServerSocket.h"
#include "SocketException.h"


ServerSocket::ServerSocket ( int port )
{
  if ( ! Socket::create() )
    {
      throw SocketException ( "NO SE PUEDE CREAR EL SOCKET SERVER." );
    }

  if ( ! Socket::bind ( port ) )
    {
      throw SocketException ( "NO SE PUEDE ENLAZAR AL PORT." );
    }

  if ( ! Socket::listen() )
    {
      throw SocketException ( "NO SE PUEDE ESCUCHAR DEL SOCKET." );
    }

}

ServerSocket::~ServerSocket()
{
}


const ServerSocket& ServerSocket::operator << ( const std::string& s ) const
{
  if ( ! Socket::send ( s ) )
    {
      throw SocketException ( "NO SE PUEDE ESCRIBIR AL SOCKET." );
    }

  return *this;

}


const ServerSocket& ServerSocket::operator >> ( std::string& s ) const
{
  if ( ! Socket::recv ( s ) )
    {
      throw SocketException ( "NO SE PUEDE LEER DEL SOCKET." );
    }

  return *this;
}

void ServerSocket::accept ( ServerSocket& sock )
{
  if ( ! Socket::accept ( sock ) )
    {
      throw SocketException ( "NO SE PUEDE ACEPTAR EL SOCKET." );
    }
}